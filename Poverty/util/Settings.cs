        virtual public bool IsEnabled<T>()
        {
            if (typeof(T) == typeof(ContainerEntry))
                return ContainerItem.Enable;

            if (typeof(T) == typeof(Container))
                return Container.Enable;

            if (typeof(T) == typeof(Flora))
                return Flora.Enable;

            if (typeof(T) == typeof(LeveledItem))
                return LeveledItem.Enable;

            if (typeof(T) == typeof(Npc))
                return NPC.Enable;

            if (typeof(T) == typeof(Tree))
                return Tree.Enable;
            return false;
        }

        virtual public bool CheckAndResolve(ContainerEntry entry, ILinkCache linkCache, out IItemGetter? link)
        {
            if (linkCache.TryResolve<IItemGetter>(entry.Item.Item.FormKey, out link))
                return this[link];
            return false;
        }

        virtual public bool this[SkyrimMajorRecord record]
        {
            get
            {
                if (record.EditorID != null)
                {
                    string id = record.EditorID;
                    Type type = record.GetType();

                    if (type == typeof(Container))
                        return Container[id];

                    if (type == typeof(Flora))
                        return Flora[id];

                    if (type == typeof(LeveledItem))
                        return LeveledItem[id];

                    if (type == typeof(Npc))
                        return NPC[id];

                    if (type == typeof(Tree))
                        return Tree[id];
                }
                return false;
            }
        }
        virtual public bool this[ISkyrimMajorRecordGetter getter]
        {
            get
            {
                if (getter.EditorID != null)
                {
                    string id = getter.EditorID;
                    Type type = getter.Type;

                    if (type == typeof(Container))
                        return Container[id];

                    if (type == typeof(Flora))
                        return Flora[id];

                    if (type == typeof(LeveledItem))
                        return LeveledItem[id];

                    if (type == typeof(Npc))
                        return NPC[id];

                    if (type == typeof(Tree))
                        return Tree[id];
                }
                return false;
            }
        }
        virtual public bool this[IItemGetter itemLink]
        {
            get
            {
                return itemLink.EditorID != null && ContainerItem[itemLink.EditorID];
            }
        }
